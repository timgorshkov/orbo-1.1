-- Migration 299: переход от «АКТ-N» к «ТВ-N» для номера документа на вывод
--
-- Документ, который мы формируем на одобренную заявку на вывод средств — это
-- НЕ акт оказанных услуг (агентское вознаграждение фиксируется отдельно
-- ежемесячным актом АВ-N), а **Требование на вывод** (термин из п. 1.12
-- агентского договора-оферты): распоряжение Принципала о перечислении
-- доступного остатка с Баланса Организатора.
--
-- Меняем только функцию генерации номера. Колонку `org_withdrawals.act_number`
-- оставляем как есть (это просто строка-идентификатор; переименование колонки
-- сломало бы код), но новые записи будут получать формат `ТВ-NNNNNN`.
-- Существующая тестовая запись «АКТ-000001» не трогаем — она помечена is_test
-- (мигр. 298) и в реестры не попадает.

CREATE OR REPLACE FUNCTION public.generate_act_number()
RETURNS text
LANGUAGE plpgsql
AS $$
BEGIN
  RETURN 'ТВ-' || LPAD(nextval('act_number_seq')::TEXT, 6, '0');
END;
$$;

COMMENT ON FUNCTION public.generate_act_number() IS
  'Генерирует номер «Требования на вывод средств» (ТВ-NNNNNN). Используется в org_withdrawals.act_number — несмотря на историческое имя, это распоряжение Принципала по п.5.4 агентского договора, не акт оказанных услуг.';

DO $$
BEGIN
  RAISE NOTICE '✅ Migration 299: generate_act_number() now produces ТВ-NNNNNN';
END $$;
